const today = new Date();
function daysAgo(n: number) {
  const d = new Date(today);
  d.setDate(d.getDate() - n);
  return d.toISOString();
}

function hoursAfter(base: string, h: number) {
  const d = new Date(base);
  d.setHours(d.getHours() + h);
  return d.toISOString();
}

